<h2>什么是可用积分</h2>
<p>
好好修车技师版APP中，用户通过参与官方活动、使用APP、分享APP、评分纠错等可以获得积分，
积分决定用户在好好修车技师版APP中的等级和权限。用户获得的积分可以被消耗，用于兑换经验值（可在汽修人板块中悬赏等）、
兑换礼品、返现等。</p>
<p>用户的总积分由可用积分和已用积分组成，计算关系是：<strong>用户积分=可用积分+已用积分</strong>。具体如下：</p>
<ol>
	<li>积分可以被消耗，但是等级不会降低。
		<p>
在好好修车技师版APP中获得的<strong>积分可以被消耗，但用户等级不会降低</strong>。
消耗积分时，用户的所有积分将被分成“可用积分”和“已用积分”。被消耗的积分将被计算到已用积分中，而不能参与后续的使用。
		</p>
	</li>
	<li>
	</li>什么是可用积分？
		<p>
可用积分是用户总积分刨除已消耗积分剩余的积分，在需要积分兑换的场合，只有可用积分可以参与消耗。
		</p>
	</li>
	<li>可用积分可以做什么？
		<p>
（1）在汽修人问答板块悬赏经验值时，需要用可用积分兑换经验值并发布悬赏。
<strong>现阶段是推广期，积分与经验值得兑换关系是“10积分=1经验值”，后续好好修车会调整兑换关系</strong>。
		</p>
		<p>
（2）可用积分还将用于后续的兑换礼品、返现等。
		</p>
	</li>
</ol>
<p>好好修车有权调整积分等级体系和经验值等级体系，敬请周知。</p>
