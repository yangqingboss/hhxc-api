<h2>好好修车技师 用户积分和等级方案（试用版）</h2>
<h3>一、用户等级概述</h3>
<ol>
	<li>用户直接从1级到100级累加，不同等级在好好修车产品中对应不同的权限。</li>
	<li>具体每个等级权限和需要的积分见《好好修车技师版用户积分和等级权限对应表》。</li>
	<li>
积分可以被消耗，但用户等级不会降低。消耗积分时，用户的所有积分将被分成“可用积分”和“已用积分”。
被消耗的积分将被计算到已用积分中，而不能参与后续的使用。
	</li>
	<li>积分可以在汽修人的问答板块悬赏时，用于兑换所需的悬赏经验值。 </li>
	<li>未来积分可以兑换好好修车币、参与商品兑换、参与返现等。</li>
	<li>用户通过不正当手段获得的积分，好好修车有权收回甚至加倍扣除。</li>
</ol>
<h3>二、如何获取积分</h3>
<p>积分周期按天计算，从每天的00:00:00到23:59:59作为计分时间周期。具体规则如下：
<ol>
	<li>
		每天登陆<br />
		每天登陆一次，可获得20积分。
	</li>
	<li>
		每天搜索问题<br />
		搜索一个问题5分，每天50分封顶。
	</li>
	<li>
		保存信息<br />
		保存信息，获得10分，每天100分封顶。
	</li>
	<li>
		对搜索结果进行纠错<br />
		纠错一条并被采纳，获得100积分，上不封顶。
	</li>
	<li>
		对搜索结果进行评分<br />
		一条获得10分，每天50分封顶。
	</li>
	<li>
		分享好好修车APP<br />
		将好好修车APP分享给微信、微博、QQ空间等平台中的好友，每次100积分，每天500分封顶。
	</li>
	<li>
		分享好好APP里的内容<br />
		将好好修车APP里的内容，分享给微信、微博、QQ空间等平台中的好友，每次20积分，每天200分封顶。
	</li>
	<li>
		完成注册信息<br />
		完成注册操作，可获得200积分。
	</li>
	<li>
		完善个人信息<br />
		完善个人信息，包括修改昵称、所在城市并上传头像，可获得200积分。
	</li>
	<li>
		完善职业信息<br />
		完善职业信息，包括擅长品牌、所属工种、现有职称和修车经历，可获300积分。
	</li>
	<li>
		提交意见反馈<br />
		提交有价值的意见反馈，每次可获得100积分，累积200分封顶。
	</li>
	<li>
		好好修车团队会根据用户贡献，如提供宝贵修改意见等，额外给予奖励积分。
	</li>
</ol>
将来会有更多获得积分的方式补充进来，敬请关注。
</p>
