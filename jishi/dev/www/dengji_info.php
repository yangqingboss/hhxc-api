<h3>LV1~LV25</h3>
<p>关于“查看具体信息条数”的解释：<br />查看信息采用每日累积的方式，每日凌晨零点清空数据重新计数，计数范围包括<br />
<ol>
	<li>
查现象中，搜索某个关键词，出现对应的现象。此时用户点击选中某一条信息，并点击开始分析后，计数一次。
（若查看详情和选中该条信息时同一条内容，则只记录一次。）
	</li>
	<li>
查案例中，用户搜索某个关键词后，出现对应的案例。此时，用户点击查看案例详情，计数一次；
点击选中后，保存信息，计数一次。（若查看详情和保存信息时同一条内容，则只记录一次）
	</li>
	<li>查故障点中，用户搜索某个关键词，出现对应的故障点。用户选中某一条后，点击了解详情，计数一次。</li>
	<li>查故障码中，对于搜索出来的信息，用户每选中一个相关度较大的故障点并点击了解详情后，计数一次。</li>
	<li>匹配正时中，用户每查看一条信息详情，计数一次。</li>
</ol>
</p>
<h3>LV25~LV39</h3>
<p>
当权限不够，无法再操作的时候，弹出提示，并将如何获取积分的规则链接提供给用户。用户查看规则后，
即时操作，可以即时获得新的积分和等级的提升。升级后，当天在低级别中累积的操作，跟随进入高级别的权限中，
并参与计数。如在lv30的查看权限是40条，升级到lv50后的权限是50条，而当日在lv30中已经查看了40条信息，
则当日只能再查看“50-40=10”条信息。过了零点后，重新拥有查看50条信息的权限。
</p>
